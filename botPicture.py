    print(f'ü§ñ ‡∏ö‡∏≠‡∏ó‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡πÅ‡∏•‡πâ‡∏ß: {bot.user}')
    print(f'üîí ‡∏ö‡∏≠‡∏ó‡∏ñ‡∏π‡∏Å‡∏à‡∏≥‡∏Å‡∏±‡∏î‡∏Å‡∏≤‡∏£‡πÉ‡∏ä‡πâ‡∏á‡∏≤‡∏ô‡πÉ‡∏ô Channel ID: {ALLOWED_CHANNEL_ID}')
    
    # üåü ‡∏ó‡∏≥‡∏Å‡∏≤‡∏£ Sync Slash Commands
